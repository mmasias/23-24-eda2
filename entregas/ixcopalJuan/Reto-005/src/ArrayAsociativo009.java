    static int capturarMovimiento() {

        switch (pedirChar()) {
            case 's', 'S', '8':
                return ABAJO;
            case 'w', 'W', '2':
                return ARRIBA;
            case 'a', 'A', '4':
                return IZQUIERDA;
            case 'd', 'D', '6':
                return DERECHA;
            case 'f', 'F':
                return SALIR;
            case 'v', 'V':
                return CAMBIA_VISUALIZACION;
            case 'g', 'G':
                return SEARCH_RUTE;
            case 'o', 'O':
                return SPECIFY_ARRIVAL_POINT;
            case 'r', 'R':
                return GO_BACK;
        }
        return NADA;
    }

