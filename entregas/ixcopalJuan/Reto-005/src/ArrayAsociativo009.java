    static List<int[]> searchRoutes(int[] start, int[] destination, String[] map){
        int rows = map.length;
        int columns = map[0].length();

        if(destination[FILA] < minFila || destination[FILA] > maxFila || destination[COLUMNA] < minColumna || destination[COLUMNA] > maxColumna){
            return new ArrayList<>();
        }

        boolean[][] visited = new boolean[rows][columns];
        List<int[]> rute = new ArrayList<>();

        if(helperSearchRoutes(start, destination, map, visited, rute)){
            return rute;
        } else {
            System.out.println("it's impossible to get there");
            return new ArrayList<>();
        }


    }

    static boolean helperSearchRoutes(int[] current, int[] destination, String[] map, boolean[][] visited, List<int[]> rute){
        if(current[FILA] == destination[FILA] && current[COLUMNA] == destination[COLUMNA] && tipoTerreno(map[destination[FILA]].charAt(destination[COLUMNA])).equals("0")){
            rute.add(current);
            return true;
        }

        visited[current[FILA]][current[COLUMNA]] = true;

        for(int[] direction : MOVIMIENTO){
            int newRow = current[FILA] + direction[FILA];
            int newColumn =  current[COLUMNA] + direction[COLUMNA];

            if(newRow >= minFila && newRow <= maxFila && newColumn >= minColumna && newColumn <= maxColumna){
                if (!visited[newRow][newColumn] && tipoTerreno(map[newRow].charAt(newColumn)).equals("0")) {
                    int[] newPosition = { newRow, newColumn };
                    if (helperSearchRoutes(newPosition, destination, map, visited, rute)) {
                        rute.add(0, current);
                        return true;
                    }
                }
            }
        }

        return false;
    }

    static void mover(int[] unPersonaje, int direccion, String[] mapa) {

        int oldFila = unPersonaje[FILA];
        int oldColumna = unPersonaje[COLUMNA];

        unPersonaje[FILA] += MOVIMIENTO[direccion][FILA];
        unPersonaje[COLUMNA] += MOVIMIENTO[direccion][COLUMNA];

        if (!tipoTerreno(mapa[unPersonaje[FILA]].charAt(unPersonaje[COLUMNA])).equals("0")) {
            unPersonaje[FILA] = oldFila;
            unPersonaje[COLUMNA] = oldColumna;
        }

        if (unPersonaje[FILA] < minFila) {
            unPersonaje[FILA] = unPersonaje[FILA] + 1;
        }
        if (unPersonaje[FILA] > maxFila) {
            unPersonaje[FILA] = unPersonaje[FILA] - 1;
        }
        if (unPersonaje[COLUMNA] < minColumna) {
            unPersonaje[COLUMNA] = unPersonaje[COLUMNA] + 1;
        }
        if (unPersonaje[COLUMNA] > maxColumna) {
            unPersonaje[COLUMNA] = unPersonaje[COLUMNA] - 1;
        }

        int[] step = {unPersonaje[FILA], unPersonaje[COLUMNA]};
        steps.add(step);
    }

    static void verAccion(int[] elPersonaje, String[] elMundo) {

        switch (capturarMovimiento()) {
            case ARRIBA:
                mover(elPersonaje, ARRIBA, elMundo);
                break;
            case ABAJO:
                mover(elPersonaje, ABAJO, elMundo);
                break;
            case IZQUIERDA:
                mover(elPersonaje, IZQUIERDA, elMundo);
                break;
            case DERECHA:
                mover(elPersonaje, DERECHA, elMundo);
                break;
            case SALIR:
                jugando = !jugando;
                break;
            case CAMBIA_VISUALIZACION:
                cambiaVisualizacion();
                break;
            case SEARCH_RUTE:
                searchRoute(elPersonaje, elMundo);
                break;
            case SPECIFY_ARRIVAL_POINT:
                specifyArrivalPoint();
                break;
            case GO_BACK:
                undo_steps(elPersonaje, elMundo);
                break;
            case NADA:
                break;
        }
    }

    static void undo_steps(int[] elPersonaje, String[] elMundo){
        while (!steps.isEmpty()) {
            int[] lastStep = steps.remove(steps.size() - 1);
            elPersonaje[FILA] = lastStep[FILA];
            elPersonaje[COLUMNA] = lastStep[COLUMNA];
            imprimirMundo(elMundo, elPersonaje);
            actualizarTiempo();
            try {
                Thread.sleep(500);
            } catch (InterruptedException e){
                e.printStackTrace();
            }
        }
        imprimirMundo(elMundo, elPersonaje);
    }

