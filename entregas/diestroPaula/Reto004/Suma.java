package entregas.diestroPaula.Reto004;

public class Suma {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;
        sumaFinal(num1, num2);
    }

    public static int sumaFinal(int num1, int num2) {
        int suma = num1 + num2;
        System.out.println("La suma es: " + suma);
        return suma;
    }
}
