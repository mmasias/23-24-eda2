    static void moverPersonaje(int[] personaje, int direccion, String[] mundo) {
        int filaAnterior = personaje[FILA];
        int columnaAnterior = personaje[COLUMNA];

        personaje[FILA] += DIRECCIONES[direccion][FILA];
        personaje[COLUMNA] += DIRECCIONES[direccion][COLUMNA];

        if (!detectarTerreno(mundo[personaje[FILA]].charAt(personaje[COLUMNA])).equals("0")) {
            personaje[FILA] = filaAnterior;
            personaje[COLUMNA] = columnaAnterior;
        }

        if (personaje[FILA] < limiteFilaMin) {
            personaje[FILA] = personaje[FILA] + 1;
        }
        if (personaje[FILA] > limiteFilaMax) {
            personaje[FILA] = personaje[FILA] - 1;
        }
        if (personaje[COLUMNA] < limiteColumnaMin) {
            personaje[COLUMNA] = personaje[COLUMNA] + 1;
        }
        if (personaje[COLUMNA] > limiteColumnaMax) {
            personaje[COLUMNA] = personaje[COLUMNA] - 1;
        }
        ultimoMovimiento = direccion;
    }

    static void procesarAccion(int[] personaje, String[] mundo) {
        switch (capturarMovimiento()) {
            case ARRIBA:
                moverPersonaje(personaje, ARRIBA, mundo);
                break;
            case ABAJO:
                moverPersonaje(personaje, ABAJO, mundo);
                break;
            case IZQUIERDA:
                moverPersonaje(personaje, IZQUIERDA, mundo);
                break;
            case DERECHA:
                moverPersonaje(personaje, DERECHA, mundo);
                break;
            case SALIR:
                enJuego = !enJuego;
                break;
            case CAMBIAR_VISUALIZACION:
                cambiarVisualizacion();
                break;
            case MODO_AUTOMATICO:
                modoAutomatico = true;
                break;
            case NADA:
                break;
        }
    }

    static void cambiarVisualizacion() {
        modoVisual++;
        if (modoVisual > 3) {
            modoVisual = 0;
        }
    }

    static int capturarMovimiento() {
        switch (solicitarCaracter()) {
            case 's', 'S', '8':
                return ABAJO;
            case 'w', 'W', '2':
                return ARRIBA;
            case 'a', 'A', '4':
                return IZQUIERDA;
            case 'd', 'D', '6':
                return DERECHA;
            case 'b', 'B':
                return MODO_AUTOMATICO;
            case 'f', 'F':
                return SALIR;
            case 'v', 'V':
                return CAMBIAR_VISUALIZACION;
        }
        return NADA;
    }

    static char solicitarCaracter() {
        Scanner entrada = new Scanner(System.in);
        String inputUsuario = entrada.nextLine() + "x";
        return inputUsuario.charAt(0);
    }

