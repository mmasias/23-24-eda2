    static int[][] crearLaberinto(String[] mapa){
        int[][] laberinto = new int[mapa.length][mapa[0].length()];
        for (int i = 0; i < mapa.length; i++) {
            for (int j = 0; j < mapa[i].length(); j++) {
                if (mapa[i].charAt(j) == ' ' || mapa[i].charAt(j) == '-' || mapa[i].charAt(j) == '|') {
                    laberinto[i][j] = MURO;
                } else {
                    laberinto[i][j] = LIBRE;
                }
            }
        }
        return laberinto;
    }

